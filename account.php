二、分卷压缩 1.使用tar分卷压缩 格式： tar zcvf - filedir | split -d -b 500m - filename 
样例： tar zcvf - ./picture | split -d -b 500m - picture 
将./picture打包，并切割为 500m 的包 输出的文件为 filename00、filename01、filename02 … 
假设不加filename，则输出文件为 x00、x01、x02 … 假设不加參数 -d。则输出aa、ab、ac …

